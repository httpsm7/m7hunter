class ReportGenerator:
    def __init__(self, pipeline):
        self.p   = pipeline
        self.f   = pipeline.files
        self.out = pipeline.out
        self.pfx = pipeline.prefix

    def generate(self) -> str:
        target   = self.p.target
        findings = self.p.findings
        elapsed  = time.time() - self.p.start_t
        scan_date= time.strftime("%Y-%m-%d %H:%M:%S")

        subs  = count_lines(self.f["subdomains"])
        live  = count_lines(self.f["live_hosts"])
        urls  = count_lines(self.f["urls"])
        ports = count_lines(self.f["open_ports"])
        js_s  = count_lines(self.f["js_secrets"])
        crit  = sum(1 for f in findings if f["severity"]=="critical")
        high  = sum(1 for f in findings if f["severity"]=="high")
        med   = sum(1 for f in findings if f["severity"]=="medium")
        low   = sum(1 for f in findings if f["severity"]=="low")

        html_path = os.path.join(self.out, f"{self.pfx}_report.html")
        json_path = os.path.join(self.out, f"{self.pfx}_findings.json")
        md_path   = os.path.join(self.out, f"{self.pfx}_report.md")

        with open(html_path,"w") as f:
            f.write(self._html(target,scan_date,elapsed,subs,live,urls,ports,js_s,
                                crit,high,med,low,findings))

        with open(json_path,"w") as f:
            json.dump({
                "tool":"M7Hunter v3.0","author":"Sharlix | MilkyWay Intelligence",
                "target":target,"scan_date":scan_date,"elapsed":round(elapsed,1),
                "stats":{"subdomains":subs,"live_hosts":live,"urls":urls,
                         "open_ports":ports,"js_secrets":js_s},
                "findings":findings
            },f,indent=2)

        with open(md_path,"w") as f:
            f.write(self._markdown(target,scan_date,elapsed,findings,subs,live))

        self.p.log.success(f"HTML   → {html_path}")
        self.p.log.success(f"JSON   → {json_path}")
        self.p.log.success(f"MD     → {md_path}")
        return html_path

    def _markdown(self, target, scan_date, elapsed, findings, subs, live):
        lines = [
            f"# M7Hunter v3.0 — Security Report",
            f"**Target:** {target}  ",
            f"**Date:** {scan_date}  ",
            f"**Duration:** {elapsed:.0f}s  ",
            f"**Subdomains:** {subs} | **Live Hosts:** {live}  ",
            f"**Total Findings:** {len(findings)}",
            "",
            "## Findings",
            "",
            "| Severity | Type | URL | Detail |",
            "|----------|------|-----|--------|",
        ]
        for f in sorted(findings, key=lambda x: ["critical","high","medium","low","info"].index(x.get("severity","info"))):
            url = f.get("url","")[:80]
            det = f.get("detail","")[:100]
            lines.append(f"| {f['severity'].upper()} | {f['type']} | `{url}` | {det} |")
        lines += ["","---","*Generated by M7Hunter v3.0 | MilkyWay Intelligence | Sharlix*"]
        return "\n".join(lines)

