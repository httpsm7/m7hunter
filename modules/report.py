    def _file_grid(self):
        items = []
        for key,path in self.f.items():
            if "dir" in key or not path.endswith(".txt"):
                continue
            if os.path.isfile(path):
                n    = count_lines(path)
                name = os.path.basename(path)
                items.append(
                    f"<div class='fi'><span class='fn'>{name}</span>"
                    f"<span class='fc'>{n} lines</span></div>")
        return "\n".join(items) if items else "<p style='color:var(--dim)'>No files yet.</p>"
